import { MetadataRoute } from 'next'
 
export default function robots(): MetadataRoute.Robots {
  return {
    rules: [
      {
        userAgent: '*',
        allow: '/',
        disallow: ['/dashboard', '/auth', '/settings', '/projects', '/history', '/todo', '/invoicing'],
      },
    ],
    sitemap: 'https://evidujcas.cz/sitemap.xml',
  }
}

